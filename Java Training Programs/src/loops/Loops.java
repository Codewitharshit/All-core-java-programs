package loops;

public class Loops {

	public static void main(String[] args) {
		
		  int i=-3;
		System.out.println("Using while loop");
		while(i<=3)
		{
			System.out.println(i);
			i++;
		}
		
		System.out.println("Using do while loop");
		
		
		int b=2;
		do {
			System.out.println(b);
			b++;
		} while(b<5);
		
		
		
		

	}
	

}
